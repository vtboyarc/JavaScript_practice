// Learn how to use the compound assignment operators. What do they do?
// +=
// -=
// *=
// /=
// %=

// adds 1 to num1
var num1 = 1
num1 += 1
console.log(num1)

// subtracts 1 to num1
var num1 = 5
num1 -= 1
console.log(num1)

// multiplies num1 by 5
var num1 = 5
num1 *= 5
console.log(num1)

// divides num1 by 2
var num1 = 8
num1 /= 2
console.log(num1)

// returns the remainder of num1 when divided by 2, in this case, should return 1
var num1 = 7
num1 %= 2
console.log(num1)